import Foundation
import UIKit

extension UIColor {
	
    public class func busnellOrangeColor() -> UIColor {
        return UIColor(red: 254/0xFF, green: 108/0xFF, blue: 39/0xFF, alpha: 1.0)
    }
    
    public class func silverColor() -> UIColor {
        return UIColor(red: 196/0xFF, green: 204/0xFF, blue: 213/0xFF, alpha: 1.0)
    }
    
    public class func bushnellGrayColor() -> UIColor {
        return UIColor(red: 85/0xFF, green: 88/0xFF, blue: 91/0xFF, alpha: 1.0)
    }
	
}
